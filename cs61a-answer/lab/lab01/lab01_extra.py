def falling(n, k):
    """Compute the falling factorial of n to depth k.

    >>> falling(6, 3)  # 6 * 5 * 4
    120
    >>> falling(4, 0)
    1
    >>> falling(4, 3)  # 4 * 3 * 2
    24
    >>> falling(4, 1)  # 4
    4
    """
    prod = 1
    while k > 0:
        prod = prod * n
        n -= 1
        k -= 1
    return prod

def double_eights(n):
    """Return true if n has two eights in a row.
    >>> double_eights(8)
    False
    >>> double_eights(88)
    True
    >>> double_eights(2882)
    True
    >>> double_eights(880088)
    True
    >>> double_eights(12345)
    False
    >>> double_eights(80808080)
    False
    """
    last_digit = None
    while n > 0:
        current_digit =  n % 10
        n = n // 10
        if last_digit == 8 and current_digit == 8:
            return True
        else:
            last_digit = current_digit
    return False

